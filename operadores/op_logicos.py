#and (&)
#con and simepre las dos condiciones tienen que ser true,de lo contrario,va a salir false

RESULTADO1 = True & True
RESULTADO2 = False & True
RESULTADO3 = False & False
RESULTADO4 = True & False

#OR
#nos va a devolver falso solamente si las dos condiciones son falsas
RESULTADO5 = True | True
RESULTADO6 = False | True
RESULTADO7 = True | False
RESULTADO8 = False | False


#NOT
#invierte valor,si damos true,lo convierte a false

RESULTADO9 = not 2 < 10


print(RESULTADO9)




